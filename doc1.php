<h1>La surface du web dit web visible</h1>

<p>L' Internet visible est constitué du web de tous les jours, celui qui ressort dans les recherches des moteurs tels que Google, Qwant ou encore Bing : réseaux sociaux, vente en ligne, blogs, ainsi que les services de messageries pour le moment rien de bien compliquer.</p>

<h2>Le Deep-web dit Web Profond </h2>

<p>Le web profond, appelé aussi toile profonde ou web invisible décrit dans l'architecture du web la partie de la toile non indexée par les principaux moteurs de recherche généralistes. Ce terme est parfois aussi utilisé de manière abusive pour désigner les contenus choquants et peu visibles que l'on trouve sur le web.
Document gouvernementaux , données financiére, informations médical, docment scientifiques et d'autres données restreintes. Les contenus sont bien entendu pas disponible en libre d'accès et demande un mot de passe pour étre consultés. 
</p>

<h3> Le Dark Web</h3>

<p>Ce qui distingue le Dark Web, en plus de son accès restreint, est l’anonymat maximal des utilisateurs. Les sites que vous consultez et les personnes avec qui vous communiquez restent à l’abri des regards extérieurs et des moteurs de recherche. Grâce aux cryptomonnaies telles que lebitcoin, les opérations de paiement sont également traitées de manière totalement anonyme.
En théorie, il est possible de trouver des offres totalement légales sur le Dark Web, à condition qu’une version .onion correspondante ait été créée à cette fin. Ainsi, des internautes résidant dans un pays très réglementé et désireux d’accéder à Twitter, Facebook, etc. pourraient le faire par l’intermédiaire du Dark Web. Les journalistes, opposants politiques et lanceurs d’alerte peuvent profiter de l’anonymat pour diffuser des informations et s’organiser. Le Dark Web laisse donc de la place pour des activités « positives ».
Cependant, la réalité est assez différente. La vente de données volées et de drogue reste l’une des activités les moins graves pratiquées sur les places de marché et forums du Dark Web. En effet, le grand banditisme, le trafic d’armes et la traite d’êtres humains sont également présents sur le Darknet.</p>


    <h1>Les VPN (Réseau privé virtuel)
    <div class="titre">Pourquoi utiliser un VPN ?</div></h1>
    <p>En informatique, un réseau privé virtuel ou réseau virtuel privé, plus communément abrégé en VPN, est un système permettant de créer un lien direct entre des ordinateurs distants, qui isole leurs échanges du reste du trafic se déroulant sur des réseaux de télécommunication publics.</p>   
    <h2>Comment fonctionne un VPN ?</h2>
    <P>Un réseau virtuel privé, ou VPN, est un système qui réalise une connexion internet à l'aide d'un autre serveur. Il est situé, la plupart du temps, dans un autre pays, par le biais d'une connexion chiffrée. De cette manière, ce VPN vous fait bénéficier d'une adresse IP différente de celle qui vous est normalement attribuée. Vous pouvez ainsi cacher votre situation géographique réelle et surfer sur internet sous couvert d'anonymat.
    Utilisé, à la base, principalement par des entreprises, de plus en plus de particuliers l'emploient aujourd'hui. Lorsque les employés voulaient accéder à l'intranet de leur entreprise à distance, et ce, de manière sécurisée, les VPN étaient un recours parfait.</P>
    <h3>Les avantages d'utiliser un VPN</h3>
     <p>Les données qui circulent entre votre ordinateur et le serveur privé sont cryptées. De cette manière, le fournisseur d'accès internet ne peut pas atteindre ces informations qui passent par votre connexion.
      Votre adresse IP est modifiée et vous pouvez ainsi être localisé en France ou à l'étranger. Cela permet, parfois, de contourner des restrictions géographiques en vigueur sur des contenus.</p>

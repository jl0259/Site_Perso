<h1>Utiliser Tor pour être anonyme sur le web</h1></div><br>
<h2>Installation du logiciel Tor</h2>
<p>Commencez par ouvrir votre navigateur habituel  et cliquer sur le lien pour télécharger Tor en prenant soin de bien choisir son OS.<a href="https://www.torproject.org/fr"target="_blanck">https://www.torproject.org/fr/download</p></a>
<p>Une fois le fichier téléchargé, ouvrez-le et il vous demandera de choisir un dossier de destination. Choisissez un dossier (par exemple le dossier des téléchargements) et attendez qu’il soit décompressé, une fois cette étape terminée, Tor est prêt à être utilisé.</p>

<h3>Lancement de Tor</h3>
<p>Lors de ce premier lancement, il faut cliquer sur se connecter à Tor, patienter que la connection se fasse et que le navigateur s’ouvre.
TorBrowser est maintenant lancé,vous pouvez dores-et-déjà commencer à surfer anonymement, mais il est recommandé de faire un petit réglage avant.</p>
<h4>Activer No-Script</h4>
<p>Tor est un réseau d’anonymisation. Cela veut dire qu’il cache votre identité à tous les intermédiaires se trouvant entre vous et le site web que vous visitez. Tout cet anonymat peut tomber si vous utilisez un programme qui outre-passe Tor ou qui envoie des données sur vous sur le net. Parmi ces programmes se trouvent les scripts dans les pages web.</p>
<p>Les scripts JavaScript sont un constituant normal et légitime des pages web, mais malheureusement, certains sites utilisent le JavaScript pour vous traquer, tracer ou espionner.
Le plugin NoScript (pré-installé dans TorBrowser) est là pour bloquer tous les scripts par défaut. Certaines pages vont alors mal fonctionner, mais au moins votre anonymat ne sera pas compromise, et l’anonymat du réseau Tor dans son ensemble non plus.Il est essentiel de bloquer les scripts dans les pages web quand on utilise le réseau Tor.</p>
<p>Cliquez sur l’icône de NoScript en haut, et vérifiez que l’icône du S est bien barré. Autrement, il faut cliquer dessus et activer le bloquage des scripts.Lorsque tout ceci est fait, vous pouvez utiliser TorBrowser comme vous utilisez votre navigateur normalement.</p>
<h5>Surfer anonymement !</h5>
<p>TorBrowser est un navigateur comme un autre, vous pouvez aller sur n’importe quel site avec. Notez quand même que si vous voulez rester anonyme, évitez de vous connecter à Facebook, Google, Gmail : tout votre anonymat tomberait alors à l’eau et Tor ne servira pratiquement à rien.</p>
<p>Vous pouvez aller sur cette page et vérifier que vous disposez bien d’une adresse IP anonyme qui n’est pas votre IP habituelle <a href="https://lehollandaisvolant.net/tout/tools/browser/"target="_blanck">:Informations IP</p></a>
<p>Le fonctionnement de Tor n’est pas abordé ici, mais sachez que Tor change votre adresse IP à interval régulier (5~10 minutes) : si vous revenez sur le lien Information IP juste au dessus, vous verez qu’elle change</p>







